#include <unistd.h>

int	main(int agrc, char **agrv)
{
	int	i;
	int	j;

	j = agrc - 1;
	while (j > 0)
	{
		i = 0;
		while (agrv[j][i] != '\0')
		{
			write(1, &agrv[j][i], 1);
			i++;
		}
		j--;
		write(1, "\n", 1);
	}
}
